# area of triangle 

base = int(input("Enter Base: "))
height = int(input("Enter Height: "))

AreaOfTri = 0.5 * base * height

print("Area of Triangle is: ",AreaOfTri)

# area of rectangle 

length = int(input("Enter side length: "))
breadth = int(input("Enter breadth: "))

AreaOfRect = length * breadth

print("area of rectangle:",AreaOfRect)